const z2ui5_if_app = require("../z2ui5/02/z2ui5_if_app");
const z2ui5_cl_xml_view = require("../z2ui5/02/z2ui5_cl_xml_view");

class z2ui5_cl_demo_app_234 extends z2ui5_if_app {


  client = null;

view_display() {
const page = z2ui5_cl_xml_view.factory( ).shell(
         ).page({ title: `abap2UI5 - Sample: TextArea - Value States`, navbuttonpress: this.client._event_nav_app_leave( ), shownavbutton: this.client.check_app_prev_stack( ) });

    const layout = page.vertical_layout({ class: `sapUiContentPadding`, width: `100%` }).content( `layout`
                              ).text_area({ valuestate: `Warning`, placeholder: `ValueState : Warning`, width: `100%` }).text_area({ valuestate: `Error`, placeholder: `ValueState : Error`, width: `100%` }).text_area({ valuestate: `Success`, placeholder: `ValueState : Success`, width: `100%` }).text_area({ valuestate: `Information`, placeholder: `ValueState : Information`, width: `100%` });

    this.client.view_display( page.stringify( ) );

  }
async main(client) {
if (this.client.check_on_init( )) {

      view_display( this.client );
    }
}
}

module.exports = z2ui5_cl_demo_app_234;
